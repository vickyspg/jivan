                    <p>They are required to file a declaration in form IPI 7 with Central Office of Reserve Bank at Mumbai within a period of 90 days from the date of purchase of immovable property or final payment of purchase consideration alongwith a certified copy of the document evidencing the transaction and bank certificate regarding the consideration paid.</p>
