                    <p>Check the distance from facilities like schools, colleges, transport facilities, markets, hospitals, etc.</p>
                    <p>Find out the general profile of owners in the building.</p>
                    <p>Visit the developer's earlier projects to check out the quality of the construction, landscaping, and other amenities. Find out about the architects and the contractors.</p>
